void DisplayFactors(int iNo                    //Input
                    )
{   
    int iCnt=0;                                 
    if(iNo<0)
    {
        iNo=-iNo;
    }
    for(iCnt=1;iCnt<iNo;iCnt++)
    {
        if ((iNo % iCnt ) == 0)
        {
            printf("%d\n",iCnt);
        }
    }
}
//End Of DisplayFactors Function

//Time Complexity : O(N)

///////////////////////////////////////////////////////////////////////////////////////////////
//
//   Entry point function of appliaction
//
///////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;

    printf("Enter First Number\n");
    scanf("%d", &iValue);

    DisplayFactors(iValue);

    return 0;
}
